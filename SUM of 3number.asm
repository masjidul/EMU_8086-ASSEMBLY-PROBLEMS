INCLUDE 'EMU8086.INC'
DATA SEGMENT
B    DB ?
H    DB ?
R    DB ?
RES  DB 10 DUP ('$')
MSG1 DB "ENTER NUM1 : $"
MSG3 DB "ENTER NUM2 : $"
MSG4 DB "ENTER NUM3 : $"
MSG2 DB 10,13,"SUM : $"

DATA ENDS

CODE SEGMENT
ASSUME DS:DATA,CS:CODE

START:
    MOV AX,DATA
    MOV DS,AX
    
    LEA DX,MSG1
    MOV AH,9
    INT 21H
    
    MOV AH,1
    INT 21H
    SUB AL,30H
    MOV B,AL
    MOV AH,0
    
    PRINTN
    LEA DX,MSG3
    MOV AH,9
    INT 21H
    
    MOV AH,1
    INT 21H
    SUB AL,30H
    MOV H,AL
    MOV AH,0 
    
    PRINTN
    LEA DX,MSG4
    MOV AH,9
    INT 21H
    
    MOV AH,1
    INT 21H
    SUB AL,30H
    MOV R,AL
    MOV AH,0
    
    MOV AL, B
    ADD AL, R
    MOV RES, AL
    MOV AL, RES
    ADD AL, H
    MOV RES, AL
    
    MOV BH, RES
    
    MOV AH, BH
    MOV BL, 2
    MUL BL
    MOV RES, BL
    
    LEA SI,RES
    CALL HEX2DEC
    LEA DX,MSG2
    MOV AH,9
    INT 21H
    LEA DX,RES
    MOV AH,9
    INT 21H
    
MOV AH,4CH
INT 21H
CODE ENDS

    HEX2DEC PROC NEAR
        
    MOV CX,0
    MOV BX,10
    
   LOOP1: MOV DX,0
    DIV BX
    ADD DL,30H
    PUSH DX
    INC CX
    CMP AX,9
    JG LOOP1
    ADD AL,30H
    MOV [SI],AL
    
   LOOP2: POP AX
    INC SI
    MOV [SI],AL
    LOOP LOOP2
   RET
HEX2DEC ENDP
END START
